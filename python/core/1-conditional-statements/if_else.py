"""
if else statement : "if-else statement" in programming, specifically in Python, is used to execute one block of code if a condition is true, and another block of code if the condition is false. This allows for decision-making within the program flow based on the evaluation of a boolean expression.

syntax :

if condition:
    code block to execute if condition is true
else:
    code block to execute if condition is false
"""

# Example 1: Using if-else statement to check if a number is greater than 10

number = 9

if number > 10:
    print("The number is greater than 10.")
else:
    print("The number is not greater than 10.")

# Example 2: Using if-else statement

age = 18

if age >= 18:
    print("You are eligible to vote.")
else:
    print("You are not eligible to vote.")

